// MARK: - AnchorPoint

/// An anchor that can be used to position a view relative to another view.
public enum AnchorPoint {
    /// An anchor which positions a view near the top of a corresponding view.
    case top
    /// An anchor which positions a view in the center of a corresponding view.
    case center
    /// An anchor which positions a view near the bottom of a corresponding view.
    case bottom
}
